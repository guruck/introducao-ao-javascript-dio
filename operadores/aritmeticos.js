// Modulo (%)
// Operador binario, retorna o inteiro resto da divisão de dois operandos
console.log(`12 % 5 = ${12%5}`); //retorno => 2

let x = 0;
// Incremento (++) Decremento (--)
console.log(++x); //pre-fixado, executa antes, atribui uma unidade a x 
console.log(x++); //pre-fixado, executa antes, atribui uma unidade a x 
console.log(x);   //retorno imprime 1 1 2

//Adição (+) Subtração (-)
console.log(-3);
console.log(+"3");   //retorna 3
console.log(+true);  //retorna 1
console.log(+false); //retorna 0
console.log(-true);  //retorna -1

//exponenciação (**)
console.log(2 ** 3);    //retorna 8
console.log(10 ** -1);  //retorna 0.1

//Operador de agrupamento ()
console.log(2* (3+2));  // retorna 10
